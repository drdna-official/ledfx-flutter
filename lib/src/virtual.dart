enum TransitionMode { add }

class VirtualConfig {
  String name;
  String deviceID;
  bool autoGenerated;
  int minFreq;
  int maxFreq;
  String segmentMapping;
  int? grouping;
  double maxBrightness;
  int centerOffset;
  bool previewOnly;
  double transitionTime;
  TransitionMode transitionMode;
  int rows;
  VirtualConfig({
    required this.name,
    required this.deviceID,
    required this.autoGenerated,
    this.minFreq = MIN_FREQ,
    this.maxFreq = MAX_FREQ,
    this.segmentMapping = "span", // span | copy
    this.grouping,
    this.maxBrightness = 1.0,
    this.centerOffset = 1,
    this.previewOnly = false,
    this.transitionTime = 0.4,
    this.transitionMode = TransitionMode.add,
    this.rows = 1,
  });

  Map<String, dynamic> toJson() {
    return {
      'name': name,
      'deviceID': deviceID,
      'autoGenerated': autoGenerated,
      'minFreq': minFreq,
      'maxFreq': maxFreq,
      'mapping': segmentMapping,
      'grouping': grouping,
      'maxBrightness': maxBrightness,
      'centerOffset': centerOffset,
      'previewOnly': previewOnly,
      'transitionTime': transitionTime,
      'transitionMode': transitionMode.name,
      'rows': rows,
    };
  }

  factory VirtualConfig.fromJson(Map<String, dynamic> json) {
    return VirtualConfig(
      name: json['name'],
      deviceID: json['deviceID'],
      autoGenerated: json['autoGenerated'],
      minFreq: json['minFreq'] ?? MIN_FREQ,
      maxFreq: json['maxFreq'] ?? MAX_FREQ,
      segmentMapping: json['mapping'] ?? "span",
      grouping: json['grouping'],
      maxBrightness: json['maxBrightness'] ?? 1.0,
      centerOffset: json['centerOffset'] ?? 1,
      previewOnly: json['previewOnly'] ?? false,
      transitionTime: json['transitionTime'] ?? 0.4,
      transitionMode: json['transitionMode'] != null
          ? TransitionMode.values.firstWhere((e) => e.name == json['transitionMode'])
          : TransitionMode.add,
      rows: json['rows'] ?? 1,
    );
  }
}

class SegmentConfig {
  final String deviceID;
  final int start;
  final int end;
  final bool inverted;
  SegmentConfig(this.deviceID, this.start, this.end, this.inverted);

  Map<String, dynamic> toJson() {
    return {'deviceID': deviceID, 'start': start, 'end': end, 'inverted': inverted};
  }

  factory SegmentConfig.fromJson(Map<String, dynamic> json) {
    return SegmentConfig(json['deviceID'], json['start'], json['end'], json['inverted']);
  }
}

